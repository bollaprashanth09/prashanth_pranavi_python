# Define two variables
a = 10
b = 20

# Equal to
print(f"{a} == {b} : {a == b}")

# Not equal to
print(f"{a} != {b} : {a != b}")

# Greater than
print(f"{a} > {b} : {a > b}")

# Less than
print(f"{a} < {b} : {a < b}")

# Greater than or equal to
print(f"{a} >= {b} : {a >= b}")

# Less than or equal to
print(f"{a} <= {b} : {a <= b}")

# Additional example with strings
str1 = "hello"
str2 = "world"

# String comparisons
print(f"'{str1}' == '{str2}' : {str1 == str2}")
print(f"'{str1}' != '{str2}' : {str1 != str2}")
print(f"'{str1}' > '{str2}' : {str1 > str2}")
print(f"'{str1}' < '{str2}' : {str1 < str2}")
